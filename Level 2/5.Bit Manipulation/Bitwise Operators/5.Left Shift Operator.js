/*
Left Shift Operator (<<) Multiply by 2 power i
2 to power zero = 1
2 to power One  = 2
2 to power Two  = 4
*/
console.log(12 << 2);

/*
12 => 1 1 0 0
after using left shift 1 => 1 1 0 0 0 => 24     (12*2)
after using left shift 2 => 1 1 0 0 0 0 => 48   (12*4)
after using left shift 3 => 1 1 0 0 0 0 0 => 96 (12*8)
*/
